# Built in Datastructure used to store a collection of items
# -They are ordered
# -They are mutable
# -Allow duplicates

integers = [1, 2, 3, 4]
print(integers)

stringList = ['Milk', 'Cheese', 'Butter']
print(stringList)

# Lists can store different data types

mixeList = [1, 'spam', 2.33]
print(mixeList)

nestedList = [1, 2, 4, 5, ['test', 'milky', 'way']]
print(nestedList)

empty = []
print(empty)
